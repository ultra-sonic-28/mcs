// DrawSmallText renders a string using the 5x7 bitmap font.
func DrawSmallText(dest *ebiten.Image, text string, x, y int, clr color.Color) {
	for i, r := range text {
		glyph, ok := SmallFont5x7[r]
		if !ok {
			glyph = SmallFont5x7['?']
		}

		for row := 0; row < 7; row++ {
			rowBits := glyph[row]
			for col := 0; col < 6; col++ {
				if (rowBits & (0x20 >> uint(col))) != 0 {
					dest.Set(x+i*6+col, y+row, clr)
				}
			}
		}
	}
}

